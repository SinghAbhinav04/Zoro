"""# tokenization"""

def tokenize_dataset(data):
  result = tokenizer(data["text"] , truncation=True , max_length=512)
  result["labels"]= result["input_ids"].copy()
  return result

train_dataset = dataset.map(tokenize_dataset , batched=False)

